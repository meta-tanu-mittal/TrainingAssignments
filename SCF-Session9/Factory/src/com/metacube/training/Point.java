package com.metacube.training;

public class Point {
	private int x;
	private int y;
	/**
	 * constructor
	 * @param x is x coordinate
	 * @param y is y coordinate
	 */
	public Point(int x,int y)
	{
		this.x=x;
		this.y=y;
	}
	
	/**
	 * @return x coordinate
	 */
	public int getX()
	{
		return x;
	}
	
	/**
	 * @return y coordinate
	 */
	public int getY()
	{
		return y;
	}


}
